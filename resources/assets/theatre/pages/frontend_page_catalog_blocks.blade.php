        <p>Soprano Sonya Yoncheva is having a very busy Met season, starting on New Year’s Eve, when she sings the title role of Puccini’s Tosca for the first time. In February, she returns as Mimì in Puccini’s La Bohème, and she takes on a third leading lady in March with the title role of Verdi’s Luisa Miller. With rehearsals for Tosca underway, she spoke with the Met’s Jay Goodwin about embodying what she describes as Puccini’s “completely passionate” title diva</p>
        <p>With Tosca, you’re making your role debut as one of opera’s iconic characters in a new production at the Met. No pressure! How are you preparing?</p>
        <p>Well, maybe it’s a matter of fate, but all of my big debuts have happened at the Met. So it feels like this is just the next one. I love singing at the Met, and I love the Met audience, so I’m very happy about it. And it’s a fantastic role that I’ve always wanted to perform. Musically, I’ve been working a very long time on the part because in 2014, while I was in the last month of pregnancy with my son and had nothing to do, I attended almost 40 days of Tosca rehearsals that my husband [Domingo Hindoyan] was conducting in Berlin. That was the moment I decided that I would sing this part.</p>
